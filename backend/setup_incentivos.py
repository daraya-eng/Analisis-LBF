MESES = ["ENE","FEB","MAR","ABR","MAY","JUN","JUL","AGO","SEP","OCT","NOV","DIC"]

def setup():
    conn = get_conn()
    cur = conn.cursor()

    cur.execute("IF OBJECT_ID('Targets_Config','U') IS NOT NULL DROP TABLE Targets_Config")
    cur.execute("IF OBJECT_ID('Targets_Pagos','U') IS NOT NULL DROP TABLE Targets_Pagos")
    # Limpiar tablas antiguas si existen
    cur.execute("IF OBJECT_ID('Incentivos_Config','U') IS NOT NULL DROP TABLE Incentivos_Config")
    cur.execute("IF OBJECT_ID('Incentivos_Anticipos','U') IS NOT NULL DROP TABLE Incentivos_Anticipos")

    cur.execute("""
        CREATE TABLE Targets_Config (
            ID               INT IDENTITY PRIMARY KEY,
            ANO              INT NOT NULL,
            VENDEDOR         VARCHAR(50) NOT NULL,
            NOMBRE           VARCHAR(100),
            TIPO             VARCHAR(30),
            BONO_VENTA_100   BIGINT,
            BONO_MARGEN_100  BIGINT,
            META_VENTA_ENE BIGINT, META_VENTA_FEB BIGINT, META_VENTA_MAR BIGINT,
            META_VENTA_ABR BIGINT, META_VENTA_MAY BIGINT, META_VENTA_JUN BIGINT,
            META_VENTA_JUL BIGINT, META_VENTA_AGO BIGINT, META_VENTA_SEP BIGINT,
            META_VENTA_OCT BIGINT, META_VENTA_NOV BIGINT, META_VENTA_DIC BIGINT,
            META_MARGEN_Q1 BIGINT, META_MARGEN_Q2 BIGINT,
            META_MARGEN_Q3 BIGINT, META_MARGEN_Q4 BIGINT
        )
    """)

    cur.execute("""
        CREATE TABLE Targets_Pagos (
            ID              INT IDENTITY PRIMARY KEY,
            ANO             INT NOT NULL,
            TRIMESTRE       INT NOT NULL,
            VENDEDOR        VARCHAR(50) NOT NULL,
            ANTICIPO_VENTA  BIGINT DEFAULT 0,
            PAGADO          BIT DEFAULT 0,
            FECHA_PAGO      DATE
        )
    """)

    cols_venta = " ".join(f"META_VENTA_{m}," for m in MESES)
    for v in VENDEDORES:
        code, nombre, tipo, bv100, bm100, metas_v, metas_m = v
        mv = [m if m is not None else "NULL" for m in metas_v]
        mm = [m if m is not None else "NULL" for m in metas_m]
        cur.execute(f"""
            INSERT INTO Targets_Config
            (ANO, VENDEDOR, NOMBRE, TIPO, BONO_VENTA_100, BONO_MARGEN_100,
             META_VENTA_ENE,META_VENTA_FEB,META_VENTA_MAR,META_VENTA_ABR,
             META_VENTA_MAY,META_VENTA_JUN,META_VENTA_JUL,META_VENTA_AGO,
             META_VENTA_SEP,META_VENTA_OCT,META_VENTA_NOV,META_VENTA_DIC,
             META_MARGEN_Q1,META_MARGEN_Q2,META_MARGEN_Q3,META_MARGEN_Q4)
            VALUES (2026, ?, ?, ?, ?, ?,
                    {','.join(str(x) for x in mv)},
                    {','.join(str(x) for x in mm)})
        """, (code, nombre, tipo, bv100, bm100))

    conn.commit()
    conn.close()
    print("Tablas creadas y datos cargados correctamente.")

if __name__ == "__main__":
    setup()
